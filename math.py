#abs() => absolute value of the number
print(abs(-10))

print(abs(0.19))

#pow(base, exp, mod) => power 
print(pow(2, 4))

print(pow(6, 4, 2))

#min(item, item, item, or iterator) => min number
print(min(10, 39, 19, 39, 10, 30))

print(min("X", "Y", "A", "F", "Hibah", "Jane" ))

numbers = (1, 3, 2, 5, -19, 299, 19)

print(min(numbers))

#max(item, item, item, or iterator) => max number
print(max(numbers))

print(max("X", "Y", "A", "F", "Hibah", "Jane" ))

#slice() 
x = [1, 4, 29, 10, 2, 10, 3, 2, 19]

print(x[slice(2, 5)])

print(x[slice(3, 8)])

